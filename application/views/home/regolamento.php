                            <h4>Rigoristi</h4>
                            Ogni utente è tenuto a selezionare i propri rigoristi, nel caso in cui si renda necessario tirare i calci di rigore per stabilire il passaggio del turno in SuperCoppa, Champions, Coppa Treble o Treble League (solo per il Minitorneo Cena).<br>
                            Nel caso in cui non siano mai stati schierati i rigoristi, il Sistema provvederà in automatico alla selezione, partendo dagli attaccanti, centrocampisti, difensori ed infine i portieri, in rigoroso ordine alfabetico.<br>
                            Se invece i rigoristi sono stati schierati almeno una volta, verrà rispettato comunque l'ordine di selezione dell'utente. I rigoristi schierati sono validi per quella specifica giornata (di Campionato o Coppe) e rimarranno tali, per le giornate successive, finchè non verranno nuovamente modificati dall'utente.<br>
                            Sarà possibile modificare i rigoristi in qualsiasi momento, fino ad 1 secondo prima dell'orario di inizio della prima partita della giornata di Treble League in svolgimento, esattamente come accade per lo schieramento delle formazioni.<br><br>
                            In fase di esecuzione dei calci di rigore, <u>verrà preso in considerazione il voto (e non il Fantavoto)</u> dei primi 5 rigoristi titolari schierati. In caso sia superiore o uguale a 6, il calcio di rigore viene considerato come realizzato, in caso contrario è da ritenersi sbagliato.<br>
                            Nel caso in cui dopo i primi 5 calci di rigore, il punteggio sia ancora pari, si procederà ad oltranza. Se ancora il punteggio dovesse essere in parità, prevarrà il fantapunteggio totale realizzato dai rigoristi che hanno calciato i rigori.<br><br><br>


